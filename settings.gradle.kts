include("backend") // 백엔드 모듈 포함
include("frontend") // 프론트엔드 모듈 포함


//큰 집 만들기:
//우리가 큰 집을 지을 때, 여러 개의 방을 만들죠? 이 코드는 우리의 컴퓨터 프로그램을 큰 집처럼 만드는 거예요.
//방 이름 정하기:
//rootProject.name = "plus-system-ver2"는 우리 큰 집의 이름을 "plus-system-ver2"로 정하는 거예요.
//방 나누기:
//include("backend")는 우리 집에 "backend"라는 방을 만드는 거예요. 이 방은 컴퓨터가 혼자 일하는 공간이에요.
//include("frontend")는 "frontend"라는 또 다른 방을 만드는 거예요. 이 방은 사람들이 컴퓨터와 대화하는 공간이에요.
//왜 방을 나누나요?:
//큰 집에서 방을 나누면 정리하기 쉽죠? 프로그램도 마찬가지예요.
//"backend" 방에서는 컴퓨터가 열심히 계산하고 정보를 저장해요.
//"frontend" 방에서는 사람들이 보기 좋게 꾸미고 버튼도 만들어요.
//함께 일하기 쉬워요:
//방을 나누면 여러 사람이 각자의 방에서 일할 수 있어요.
//한 사람은 "backend" 방에서, 다른 사람은 "frontend" 방에서 일할 수 있죠.
//고치기 쉬워요:
//한 방에 문제가 생겨도 다른 방은 괜찮아요.
//예를 들어, "frontend" 방의 색을 바꿔도 "backend" 방의 계산은 그대로예요.
//이렇게 방을 나누면 우리의 프로그램 집이 더 깔끔하고, 여러 사람이 함께 일하기 좋고, 문제가 생겼을 때 고치기도 쉬워져요!
